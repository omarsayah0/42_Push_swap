#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*dest;
	int		len;

	len = 0;
	while (src[len] != '\0')
		len++;
	i = 0;
	dest = malloc(len * sizeof (char) + 1);
	if (!dest)
		return (0);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
